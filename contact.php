<?php
// Memulai sesi dan menghubungkan ke database
session_start();
require_once 'koneksi.php'; // Pastikan file ini mengandung koneksi ke database dengan variabel $conn

// Memeriksa apakah form telah disubmit
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    // Mengambil data dari form
    $name = htmlspecialchars(trim($_POST['name']));
    $email = htmlspecialchars(trim($_POST['email']));
    $message = htmlspecialchars(trim($_POST['message']));

    // Validasi sederhana
    if (!empty($name) && !empty($email) && !empty($message)) {
        try {
            // Query untuk memasukkan data ke dalam tabel
            $stmt = $conn->prepare("INSERT INTO contact (name, email, message) VALUES (:name, :email, :message)");
            $stmt->bindParam(':name', $name);
            $stmt->bindParam(':email', $email);
            $stmt->bindParam(':message', $message);
            $stmt->execute();
            // Set notifikasi sukses
            $_SESSION['success'] = "Pesan Anda berhasil dikirim!";
        } catch (PDOException $e) {
            // Set notifikasi error jika ada masalah
            $_SESSION['error'] = "Terjadi kesalahan: " . $e->getMessage();
        }
    } else {
        // Jika input tidak lengkap
        $_SESSION['error'] = "Semua kolom wajib diisi!";
    }
}
